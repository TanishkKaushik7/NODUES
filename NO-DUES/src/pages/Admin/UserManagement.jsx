import React, { useState, useEffect, useCallback, useMemo } from 'react';
import { 
  Search, UserPlus, Filter, MoreVertical, Edit2, 
  Trash2, Shield, Mail, Loader2, RefreshCw, Users,
  ChevronLeft, ChevronRight 
} from 'lucide-react';
import { useAuth } from '../../contexts/AuthContext';
import RegisterUserModal from './RegisterUserModal';
import DeleteConfirmModal from './DeleteConfirmModal';

const UserManagement = () => {
  const { authFetch } = useAuth();
  const [users, setUsers] = useState([]);
  const [totalUsers, setTotalUsers] = useState(0); 
  const [loading, setLoading] = useState(true);
  const [searchTerm, setSearchTerm] = useState('');
  const [roleFilter, setRoleFilter] = useState('');
  
  // --- PAGINATION STATE ---
  const [currentPage, setCurrentPage] = useState(1);
  const itemsPerPage = 10;

  const [isRegisterModalOpen, setIsRegisterModalOpen] = useState(false);
  const [editingUser, setEditingUser] = useState(null); 
  const [deletingUser, setDeletingUser] = useState(null); 
  const [isDeleteLoading, setIsDeleteLoading] = useState(false);
  const [activeActionId, setActiveActionId] = useState(null);

  const getSchoolCode = (id) => {
    const schools = { 1: "SOICT", 2: "SOE", 3: "SOM", 4: "SOBT", 5: "SOVSAS", 6: "SOLJ", 7: "SOHSS", 8: "SOAP" };
    return schools[id] || `School #${id}`;
  };

  const getDeptName = (id) => {
    const departments = {
      1: "CSE", 2: "IT", 3: "ECE", 4: "ME", 5: "CE", 6: "EE", 7: "BT", 8: "MGMT", 9: "LAW", 10: "HSS", 11: "AP", 12: "MATH", 13: "PHY",
      14: "Library", 15: "Hostel", 16: "Sports", 17: "Laboratories", 18: "CRC", 19: "Accounts"
    };
    return departments[id] || `Dept #${id}`;
  };

  const fetchUsers = useCallback(async () => {
    try {
      setLoading(true);
      const query = new URLSearchParams({ ...(roleFilter && { role: roleFilter }) }).toString();
      const response = await authFetch(`/api/admin/users?${query}`);
      
      if (response.ok) {
        const data = await response.json();
        const userList = data.users || (Array.isArray(data) ? data : []);
        setUsers(userList);
        setTotalUsers(data.total || userList.length);
        setCurrentPage(1); // Reset to page 1 on new fetch
      }
    } catch (error) {
      console.error("Failed to fetch users:", error);
    } finally {
      setLoading(false);
    }
  }, [authFetch, roleFilter]);

  useEffect(() => {
    fetchUsers();
  }, [fetchUsers]);

  // --- CLIENT SIDE FILTERING & PAGINATION LOGIC ---
  const filteredUsers = useMemo(() => {
    return users.filter(user => 
      user.name?.toLowerCase().includes(searchTerm.toLowerCase()) ||
      user.email?.toLowerCase().includes(searchTerm.toLowerCase())
    );
  }, [users, searchTerm]);

  const totalPages = Math.ceil(filteredUsers.length / itemsPerPage);
  
  const paginatedUsers = useMemo(() => {
    const start = (currentPage - 1) * itemsPerPage;
    return filteredUsers.slice(start, start + itemsPerPage);
  }, [filteredUsers, currentPage]);

  const handlePageChange = (newPage) => {
    if (newPage >= 1 && newPage <= totalPages) {
      setCurrentPage(newPage);
      setActiveActionId(null);
    }
  };

  const handleEditUser = (user) => {
    setEditingUser(user);
    setIsRegisterModalOpen(true);
    setActiveActionId(null);
  };

  const confirmDelete = async () => {
    if (!deletingUser) return;
    setIsDeleteLoading(true);
    try {
      const response = await authFetch(`/api/admin/users/${deletingUser.id}`, { method: 'DELETE' });
      if (response.ok) {
        setUsers(prev => prev.filter(u => u.id !== deletingUser.id));
        setTotalUsers(prev => prev - 1);
        setDeletingUser(null);
      }
    } catch (error) { console.error("Delete error:", error); } 
    finally { setIsDeleteLoading(false); }
  };

  const getRoleStyle = (role) => {
    const styles = {
      super_admin: 'bg-slate-900 text-white border-slate-900',
      admin: 'bg-purple-100 text-purple-700 border-purple-200',
      student: 'bg-blue-100 text-blue-700 border-blue-200',
      dean: 'bg-indigo-100 text-indigo-700 border-indigo-200',
      hod: 'bg-teal-100 text-teal-700 border-teal-200',
      staff: 'bg-emerald-100 text-emerald-700 border-emerald-200',
    };
    return styles[role] || 'bg-slate-100 text-slate-700 border-slate-200';
  };

  return (
    <div className="space-y-6 animate-in fade-in duration-500 h-full flex flex-col">
      {/* Header Area */}
      <div className="flex flex-col md:flex-row md:items-center justify-between gap-4">
        <div>
          <h1 className="text-2xl font-extrabold text-slate-800 tracking-tight">User Management</h1>
          <p className="text-slate-500 text-sm mt-1">Maintain and audit university staff, student, and authority accounts.</p>
        </div>
        <div className="flex items-center gap-2">
           <div className="hidden md:flex items-center gap-2 px-4 py-2.5 bg-blue-50 text-blue-700 border border-blue-100 rounded-xl mr-2">
              <Users className="h-4 w-4" />
              <span className="text-xs font-black uppercase tracking-widest">Total: {totalUsers}</span>
           </div>
           <button onClick={fetchUsers} className="p-2.5 bg-white border border-slate-200 rounded-xl hover:bg-slate-50 transition-all active:scale-95">
              <RefreshCw className={`h-4 w-4 text-slate-500 ${loading ? 'animate-spin' : ''}`} />
           </button>
           <button onClick={() => { setEditingUser(null); setIsRegisterModalOpen(true); }} className="flex items-center px-4 py-2.5 bg-slate-900 text-white rounded-xl text-xs font-black uppercase tracking-widest hover:bg-blue-600 transition-all shadow-lg active:scale-95">
             <UserPlus className="h-4 w-4 mr-2" /> Register User
           </button>
        </div>
      </div>

      {/* Toolbar */}
      <div className="bg-white p-4 rounded-3xl border border-slate-200 shadow-sm flex flex-col md:flex-row gap-4">
        <div className="relative flex-1 group">
          <Search className="absolute left-4 top-3.5 h-4 w-4 text-slate-400 group-focus-within:text-blue-500 transition-colors" />
          <input 
            type="text" 
            placeholder="Search users..." 
            className="w-full pl-11 pr-4 py-3.5 bg-slate-50 border border-slate-200 rounded-2xl text-sm focus:ring-4 focus:ring-blue-500/10 outline-none transition-all"
            value={searchTerm} onChange={(e) => {setSearchTerm(e.target.value); setCurrentPage(1);}}
          />
        </div>
        <div className="relative min-w-[220px]">
          <Filter className="absolute left-4 top-3.5 h-4 w-4 text-slate-400 pointer-events-none" />
          <select 
            className="w-full pl-11 pr-8 py-3.5 border border-slate-200 rounded-2xl text-xs bg-white focus:ring-4 focus:ring-blue-500/10 outline-none appearance-none font-black text-slate-600 uppercase tracking-widest cursor-pointer"
            value={roleFilter} onChange={(e) => {setRoleFilter(e.target.value); setCurrentPage(1);}}
          >
            <option value="">All Account Roles</option>
            <option value="admin">Admin</option>
            <option value="student">Student</option>
            <option value="dean">School Dean</option>
            <option value="hod">HOD</option>
            <option value="staff">Staff</option>
          </select>
        </div>
      </div>

      {/* Users Table */}
      <div className="bg-white rounded-3xl border border-slate-200 shadow-sm overflow-hidden flex-1 flex flex-col">
        <div className="overflow-x-auto">
          <table className="w-full text-sm text-left">
            <thead className="bg-slate-50/50 text-slate-400 font-bold text-[10px] uppercase tracking-widest border-b border-slate-100">
              <tr>
                <th className="px-8 py-5">Identification</th>
                <th className="px-6 py-5">Role Assigned</th>
                <th className="px-6 py-5">Unit Allocation</th>
                <th className="px-8 py-5 text-right">Settings</th>
              </tr>
            </thead>
            <tbody className="divide-y divide-slate-100">
              {loading ? (
                <tr>
                  <td colSpan="4" className="px-6 py-24 text-center">
                    <Loader2 className="h-10 w-10 animate-spin text-slate-300 mx-auto" />
                  </td>
                </tr>
              ) : paginatedUsers.length === 0 ? (
                 <tr>
                    <td colSpan="4" className="px-6 py-12 text-center text-slate-400 text-xs font-bold uppercase tracking-widest">No users found</td>
                 </tr>
              ) : paginatedUsers.map((user) => (
                <tr key={user.id} className="hover:bg-slate-50/50 transition-colors group">
                  <td className="px-8 py-4">
                    <div className="flex items-center gap-4">
                      <div className="h-10 w-10 rounded-xl bg-slate-100 flex items-center justify-center font-black text-slate-500">{user.name?.charAt(0)}</div>
                      <div>
                        <div className="font-bold text-slate-800">{user.name}</div>
                        <div className="text-[10px] text-slate-400 font-bold flex items-center gap-1"><Mail className="h-3 w-3" /> {user.email}</div>
                      </div>
                    </div>
                  </td>
                  <td className="px-6 py-4">
                    <span className={`inline-flex px-3 py-1 rounded-xl text-[9px] font-black uppercase border ${getRoleStyle(user.role)}`}>
                      {user.role?.replace('_', ' ')}
                    </span>
                  </td>
                  <td className="px-6 py-4">
                    <div className="flex flex-col">
                        <span className="font-bold text-slate-700">{user.school_id ? getSchoolCode(user.school_id) : (user.department_id ? getDeptName(user.department_id) : 'Global')}</span>
                        <span className="text-[9px] text-slate-400 font-black uppercase">{user.school_id ? 'School' : 'Department'}</span>
                     </div>
                  </td>
                  <td className="px-8 py-4 text-right">
                    <button onClick={() => setActiveActionId(activeActionId === user.id ? null : user.id)} className="text-slate-300 hover:text-slate-900 p-2 rounded-xl hover:bg-slate-100 transition-all">
                      <MoreVertical className="h-5 w-5" />
                    </button>
                    {activeActionId === user.id && (
                      <div className="relative">
                        <div className="fixed inset-0 z-20" onClick={() => setActiveActionId(null)} />
                        <div className="absolute right-0 top-2 w-44 bg-white border border-slate-100 rounded-2xl shadow-2xl z-30 py-2">
                          <button onClick={() => handleEditUser(user)} className="w-full flex items-center gap-3 px-4 py-2 text-[10px] font-black uppercase text-slate-600 hover:bg-slate-50"><Edit2 className="h-4 w-4 text-blue-500" /> Edit User</button>
                          <button onClick={() => setDeletingUser({ id: user.id, name: user.name })} className="w-full flex items-center gap-3 px-4 py-2 text-[10px] font-black uppercase text-red-600 hover:bg-red-50"><Trash2 className="h-4 w-4" /> Remove User</button>
                        </div>
                      </div>
                    )}
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>

        {/* --- PAGINATION FOOTER --- */}
        <div className="px-8 py-4 bg-slate-50/50 border-t border-slate-100 flex items-center justify-between">
          <div className="text-[10px] font-black text-slate-400 uppercase tracking-widest">
            Showing {Math.min(filteredUsers.length, (currentPage - 1) * itemsPerPage + 1)}-{Math.min(filteredUsers.length, currentPage * itemsPerPage)} of {filteredUsers.length}
          </div>
          
          <div className="flex items-center gap-2">
            <button 
              disabled={currentPage === 1}
              onClick={() => handlePageChange(currentPage - 1)}
              className="p-2 bg-white border border-slate-200 rounded-lg disabled:opacity-30 hover:bg-slate-50 transition-all"
            >
              <ChevronLeft className="h-4 w-4" />
            </button>
            
            <div className="flex items-center gap-1">
              {[...Array(totalPages)].map((_, i) => (
                <button
                  key={i + 1}
                  onClick={() => handlePageChange(i + 1)}
                  className={`w-8 h-8 rounded-lg text-[10px] font-black transition-all ${
                    currentPage === i + 1 
                    ? 'bg-slate-900 text-white' 
                    : 'bg-white border border-slate-200 text-slate-500 hover:bg-slate-50'
                  }`}
                >
                  {i + 1}
                </button>
              ))}
            </div>

            <button 
              disabled={currentPage === totalPages || totalPages === 0}
              onClick={() => handlePageChange(currentPage + 1)}
              className="p-2 bg-white border border-slate-200 rounded-lg disabled:opacity-30 hover:bg-slate-50 transition-all"
            >
              <ChevronRight className="h-4 w-4" />
            </button>
          </div>
        </div>
      </div>

      <RegisterUserModal isOpen={isRegisterModalOpen} onClose={() => { setIsRegisterModalOpen(false); setEditingUser(null); }} onSuccess={fetchUsers} initialData={editingUser} />
      <DeleteConfirmModal isOpen={!!deletingUser} onClose={() => setDeletingUser(null)} onConfirm={confirmDelete} userName={deletingUser?.name} isLoading={isDeleteLoading} />
    </div>
  );
};

export default UserManagement;